package Area;

public class FigurasGeometricas {
    private int base;
    private int altura;


    public FigurasGeometricas(int base, int altura) {
        this.base = base;
        this.altura = altura;

    }

    public int getBase() {
        return base;
    }

    public int getAltura() {
        return altura;
    }


}
