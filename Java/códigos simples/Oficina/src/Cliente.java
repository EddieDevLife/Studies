public class Cliente {
    String CPF;
    String nome;
    String endereco;
    String telefone;
    String email;

    public Cliente(String CPF, String nome, String endereco, String telefone, String email) {
        this.CPF = CPF;
        this.nome = nome;
        this.endereco = endereco;
        this.telefone = telefone;
        this.email = email;
    }



}
