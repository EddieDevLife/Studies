public class Veiculos {
    String placa;
    String modelo;
    int ano;
    String fabricante;
    String cor;

    public Veiculos(String placa, String modelo, int ano, String fabricante, String cor) {
        this.placa = placa;
        this.modelo = modelo;
        this.ano = ano;
        this.fabricante = fabricante;
        this.cor = cor;
    }
}
